def extract_metadata(text: str) -> dict:
    """Extract #let variable assignments from a .typ file."""
    meta = {}
    for m in re.finditer(r'#let\s+([\w-]+)\s*=\s*"([^"]*)"', text):
        key = m.group(1).replace("-", "_")
        meta[key] = m.group(2)
    return meta


def extract_content(text: str) -> str:
    """Extract protocol content (diagrams + reference notes), stripping preamble."""
    lines = text.split("\n")

    # Find content start: first separator block followed by PAGE comment
    content_start = None
    for i in range(len(lines) - 1):
        if re.match(r"^// =+$", lines[i].strip()) and "PAGE" in lines[i + 1]:
            content_start = i
            break

    if content_start is None:
        # Fallback: first #diagram(
        for i, line in enumerate(lines):
            if line.strip().startswith("#diagram("):
                content_start = i
                break

    if content_start is None:
        return ""

    content = "\n".join(lines[content_start:])

    # Fix icon paths: individual files are 4 levels deep, combined is 3 levels deep
    content = content.replace("../../../../icons/", "../../../icons/")

    # Remove any standalone #pagebreak() at the very start (we add our own)
    content = re.sub(r"^\s*#pagebreak\(\)\s*\n?", "", content)

    return content


