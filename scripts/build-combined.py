def state_update(meta: dict) -> str:
    """Generate a Typst state update block for a protocol."""
    return f"""
#pagebreak(weak: true)
#current-protocol.update((
  id: "{meta.get('protocol_id', '')}",
  subject: "{meta.get('protocol_subject', '')}",
  scenario: "{meta.get('scenario', '')}",
  sop: "{meta.get('sop_number', '')}",
  age: "{meta.get('age_group', '')}",
  version: "{meta.get('version', '')}",
  source: "{meta.get('source_authority', '')}",
  generation_date: "{meta.get('generation_date', '')}",
))
"""


def build_combined_typ(age_groups: list[tuple[str, Path]], output_name: str) -> Path:
    """Build a combined .typ file from protocol directories.

    age_groups: list of (label, directory) pairs, e.g. [("ADULT", adult_dir)]
    """
    protocols = []
    for label, typ_dir in age_groups:
        if not typ_dir.exists():
            print(f"Warning: {typ_dir} does not exist, skipping")
            continue
        for f in sorted(typ_dir.glob("*.typ")):
            text = f.read_text()
            meta = extract_metadata(text)
            content = extract_content(text)
            if content.strip():
                protocols.append((meta, content, f.name))

    # Sort by age group then SOP number
    def sort_key(p):
        meta = p[0]
        age = 0 if meta.get("age_group", "") == "ADULT" else 1
        sop = meta.get("sop_number", "999")
        return (age, sop)

    protocols.sort(key=sort_key)

    out_path = COMBINED_DIR / f"{output_name}.typ"
    with open(out_path, "w") as f:
        f.write(PREAMBLE)

        first = True
        for meta, content, filename in protocols:
            age = meta.get("age_group", "ADULT")
            subject = meta.get("protocol_subject", "")
            sop = meta.get("sop_number", "")

            f.write(f"\n// {'=' * 60}\n")
            f.write(f"// {age} — {subject} (SOP {sop}) [{filename}]\n")
            f.write(f"// {'=' * 60}\n")

            # For the very first protocol, don't emit a pagebreak
            # (the preamble doesn't end with content on a page)
            if first:
                # Write state update without pagebreak
                f.write(f"""
#current-protocol.update((
  id: "{meta.get('protocol_id', '')}",
  subject: "{meta.get('protocol_subject', '')}",
  scenario: "{meta.get('scenario', '')}",
  sop: "{meta.get('sop_number', '')}",
  age: "{meta.get('age_group', '')}",
  version: "{meta.get('version', '')}",
  source: "{meta.get('source_authority', '')}",
  generation_date: "{meta.get('generation_date', '')}",
))
""")
                first = False
            else:
                f.write(state_update(meta))

            f.write(content)
            f.write("\n")

    print(f"Generated: {out_path}")
    return out_path


def compile_typ(typ_path: Path):
    """Compile a .typ file to PDF."""
    pdf_path = typ_path.with_suffix(".pdf")
    print(f"Compiling: {typ_path.name} -> {pdf_path.name}")
    result = subprocess.run(
        ["typst", "compile", str(typ_path), str(pdf_path)],
        capture_output=True,
        text=True,
    )
    if result.returncode != 0:
        print(f"  ERROR: {result.stderr}")
        return False
    else:
        if result.stderr:
            # Typst warnings
            for line in result.stderr.strip().split("\n")[:10]:
                print(f"  warn: {line}")
        print(f"  OK: {pdf_path}")
        return True


def main():
    do_compile = "--compile" in sys.argv

    # Build adult-only combined
    adult_typ = build_combined_typ(
        [("ADULT", ADULT_DIR)],
        "israel-adult-combined-all-protocols",
    )

    # Build child-only combined
    child_typ = build_combined_typ(
        [("CHILD", CHILD_DIR)],
        "israel-child-combined-all-protocols",
    )

    # Build everything combined
    all_typ = build_combined_typ(
        [("ADULT", ADULT_DIR), ("CHILD", CHILD_DIR)],
        "israel-all-protocols-combined",
    )

    if do_compile:
        for typ_path in [adult_typ, child_typ, all_typ]:
            compile_typ(typ_path)


if __name__ == "__main__":
    main()
