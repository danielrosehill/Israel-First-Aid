"""Build combined Typst source files for protocol collections.

Reads individual protocol .typ files, extracts metadata and content,
generates combined .typ sources with shared preamble, continuous page
numbering, and state-based dynamic headers/footers.

Usage:
    python scripts/build-combined.py          # generate .typ sources
    python scripts/build-combined.py --compile # also compile to PDF
"""

